function headerLinks(btn) {
    if (btn == 'inicio') {
        location.href = "index.html"
    }
    if (btn == 'cadastro') {
        location.href = "cadastro.html"
    }
    if (btn == 'login') {
        location.href = "login.html"
    }
    if (btn == 'forum') {
        location.href = "forum.html"
    }
    if (btn == 'dashboard') {
        location.href = "dashboard.html"
    }
}